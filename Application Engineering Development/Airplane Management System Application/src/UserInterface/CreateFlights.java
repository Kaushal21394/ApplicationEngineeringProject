    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        String airlineID=txtAID.getText();
        String airlineName=txtAName.getText();
        String manuName=txtmanufacture.getText();
        int flightNum =Integer.parseInt(txtflight.getText());
        String model=txtModel.getText();
        String source=txtsource.getText();
        String dest=txtdestination.getText();
        String arrival=txtarrivalTime.getText();
        String departure=txtdepature.getText();
        
        Airplane airline =airplaneC.addAirplane();
        airline.setAirlineID(airlineID);
        airline.setAirlinerName(airlineName);
        airline.setArrival(arrival);
        airline.setDeparture(departure);
        airline.setFromLocation(dest);
        airline.setToLocation(source);
        airline.setFlightNumber(flightNum);
        airline.setModelNumber(model);
        airline.setManufactureName(manuName);
                
        JOptionPane.showMessageDialog(null,"Airline Successfully Created !!!");
        txtAID.setText("");
        txtAName.setText("");
        txtModel.setText("");
        txtmanufacture.setText("");
        txtflight.setText("");
        txtarrivalTime.setText("");
        txtdepature.setText("");
        txtsource.setText("");
        txtdestination.setText("");
                
    }//GEN-LAST:event_btnCreateActionPerformed

