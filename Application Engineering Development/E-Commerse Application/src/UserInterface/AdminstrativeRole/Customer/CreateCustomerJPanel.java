    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        try{
            if(!txtCustomerName.getText().isEmpty()){
                Market market=(Market) comboMarket.getSelectedItem();
                Customer p= market.addCustomer();
                p.setName(txtCustomerName.getText());
                p.setMarketName(market.getMarketName());
                JOptionPane.showMessageDialog(null, "Customer created successfully");
            }
            else{
                JOptionPane.showMessageDialog(null, "Please enter all values");
            }

        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Enter integer for Salary", "Warning", JOptionPane.WARNING_MESSAGE);
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_btnCreateActionPerformed

